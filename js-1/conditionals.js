// Ejericio colores

function color(num){
    if(num==1){
        return "The color is black"
    }else{
        if(num==2){
            return "The color is white"
        }else{
            if(num==3){
                return "The color is red"
            }else{
                return "The color is green"
            }
        }
    }
}

console.log(color(4));
